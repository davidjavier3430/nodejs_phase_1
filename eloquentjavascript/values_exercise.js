//Unary Operators
console.log(typeof 4.5);
console.log(typeof "x");

//Boolean values
//Compare with numbers
console.log(3 > 2);
console.log(3 < 2);
//Compare with string
console.log("Aardvark" < "Zoroaster");
//Others compare Operators
console.log(10 >= 17);
console.log(10 <= 17);
console.log("Itchy" != "Scratchy");
console.log("Itchy".length === 8);

//Logical operators
console.log(true && false);
console.log(true && true);
console.log(false || true);
console.log(false || false);
console.log(!false && true);
console.log(5 <= 1 ? 1 : 2);

//Automatic conversion
console.log(8 * null);
// → 0
console.log("5" - 1);
// → 4
console.log("5" + 1);
// → 51
console.log("five" * 2);
// → NaN
console.log(false == 0);
// → true
